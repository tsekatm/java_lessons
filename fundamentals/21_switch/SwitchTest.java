public class SwitchTest {

    public static void main(String[] args) {
        
        int a = 4;

        switch (a) {

        case 1:

            System.out.println("Cool 1");

            break;

        case 4:

            System.out.println("Cool 2");

            break;

        case 3:

            System.out.println("Cool 3");

            break;

        default:

            System.out.println("not Cool");

            break;


        }
    }
}